package com.bytezone.diskbrowser.visicalc;

// -----------------------------------------------------------------------------------//
abstract class ConstantFunction extends Function
// -----------------------------------------------------------------------------------//
{
  // ---------------------------------------------------------------------------------//
  ConstantFunction (Cell cell, String text)
  // ---------------------------------------------------------------------------------//
  {
    super (cell, text);
  }

  // ---------------------------------------------------------------------------------//
  @Override
  public String getType ()
  // ---------------------------------------------------------------------------------//
  {
    return "ConstantFunction";
  }
}